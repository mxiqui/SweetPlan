function eliminarDuplicados(aeropuertos) {
    const uniqueAirports = [];
    const seen = new Set();
    
    for (const aeropuerto of aeropuertos) {
        // Extraer la ciudad y el nombre del aeropuerto
        const [ciudad, codigo] = aeropuerto.split(',').map(item => item.trim());
        const identificador = `${ciudad}, ${codigo}`;

        // Comprobar si ya hemos visto este aeropuerto
        if (!seen.has(identificador)) {
            seen.add(identificador);
            uniqueAirports.push(aeropuerto);
        }
    }

    return uniqueAirports;
}

